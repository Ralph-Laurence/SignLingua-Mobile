package psu.signlinguamobile.api.apiresponse;

public class LearnerDetailsResponse
{
    public String getFullname()
    {
        return fullname;
    }

    public void setFullname(String fullname)
    {
        this.fullname = fullname;
    }

    public String getUsername()
    {
        return username;
    }

    public void setUsername(String username)
    {
        this.username = username;
    }

    public String getEmail()
    {
        return email;
    }

    public void setEmail(String email)
    {
        this.email = email;
    }

    public String getContact()
    {
        return contact;
    }

    public void setContact(String contact)
    {
        this.contact = contact;
    }

    public String getAddress()
    {
        return address;
    }

    public void setAddress(String address)
    {
        this.address = address;
    }

    public String getPhoto()
    {
        return photo;
    }

    public void setPhoto(String photo)
    {
        this.photo = photo;
    }

    public String getDateJoined()
    {
        return dateJoined;
    }

    public void setDateJoined(String dateJoined)
    {
        this.dateJoined = dateJoined;
    }

    public String getTotalTutors()
    {
        return totalTutors;
    }

    public void setTotalTutors(String totalTutors)
    {
        this.totalTutors = totalTutors;
    }

    public String getTotalClassrooms()
    {
        return totalClassrooms;
    }

    public void setTotalClassrooms(String totalClassrooms)
    {
        this.totalClassrooms = totalClassrooms;
    }

    public String getBio()
    {
        return bio;
    }

    public void setBio(String bio)
    {
        this.bio = bio;
    }

    public String getDisability()
    {
        return disability;
    }

    public void setDisability(String disability)
    {
        this.disability = disability;
    }

    public String getBookStatus()
    {
        return bookStatus;
    }

    public void setBookStatus(String bookStatus)
    {
        this.bookStatus = bookStatus;
    }

    private String fullname;
    private String username;
    private String email;
    private String contact;
    private String address;
    private String photo;
    private String dateJoined;
    private String totalTutors;
    private String totalClassrooms;
    private String bio;
    private String disability;
    private String bookStatus;
}